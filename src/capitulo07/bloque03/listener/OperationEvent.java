package capitulo07.bloque03.listener;

public class OperationEvent {

	private int result;
	private int div;
	private int base;
	private int part1;
	private int part2;
	
	
	
	

	public int getResult() {
		return result;
	}
	public void setResult(int result) {
		this.result = result;
	}
	public int getDiv() {
		return div;
	}
	public void setDiv(int div) {
		this.div = div;
	}
	public int getBase() {
		return base;
	}
	public void setBase(int base) {
		this.base = base;
	}
	public int getPart1() {
		return part1;
	}
	public void setPart1(int part1) {
		this.part1 = part1;
	}
	public int getPart2() {
		return part2;
	}
	public void setPart2(int part2) {
		this.part2 = part2;
	}
	
}
